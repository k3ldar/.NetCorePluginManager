using SimpleDB;

namespace PluginManager.DAL.TextFiles.Tables
{
	[Table(Constants.TableNameStore, CompressionType.None, CachingStrategy.Memory)]
	internal sealed class StoreDataRow : TableRowDefinition
	{
		private string _name;

		public string Name
		{
			get
			{
				return _name;
			}

			set
			{
				if (_name == value)
					return;

				_name = value;
				Update();
			}
		}
	}
}
